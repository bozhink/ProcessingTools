namespace ProcessingTools.Extensions
{
    /// <summary>
    /// Constants related to HTTP content types.
    /// </summary>
    public static class ContentTypes
    {
        /// <summary>
        /// Default content type.
        /// </summary>
        public const string Default = "text/plain; encoding='utf-8'";

        /// <summary>
        /// Default mime-subtype.
        /// </summary>
        public const string DefaultMimeSubtype = "octet-stream";

        /// <summary>
        /// Default mime-type.
        /// </summary>
        public const string DefaultMimeType = "application";

        /// <summary>
        /// JPEG.
        /// </summary>
        public const string Jpeg = "image/jpeg";

        /// <summary>
        /// JSON.
        /// </summary>
        public const string Json = "application/json";

        /// <summary>
        /// Octet stream.
        /// </summary>
        public const string OctetStream = "application/octet-stream";

        /// <summary>
        /// Plain text.
        /// </summary>
        public const string PlainText = "text/plain";

        /// <summary>
        /// URL-encoded.
        /// </summary>
        public const string UrlEncoded = "application/x-www-form-urlencoded";

        /// <summary>
        /// XML.
        /// </summary>
        public const string Xml = "application/xml";
    }
}
